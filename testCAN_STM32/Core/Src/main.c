/* USER CODE BEGIN 4 */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    // 1. Nhận bản tin CAN từ bộ đệm FIFO0
    if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rxHeader, rxData) == HAL_OK)
    {
        // 2. Kiểm tra ID 0x123 (Có thể bỏ IF để nhận tất cả bản tin)
        if (rxHeader.StdId == 0x123)
        {
            char msg[100];
            // Format chuỗi: Hiện ID và 8 byte dữ liệu
            int len = sprintf(msg, "\r\n[CAN] ID: 0x%X | Data: %02X %02X %02X %02X %02X %02X %02X %02X",
                             (unsigned int)rxHeader.StdId,
                             rxData[0], rxData[1], rxData[2], rxData[3],
                             rxData[4], rxData[5], rxData[6], rxData[7]);

            // 3. Đẩy chuỗi lên Putty thông qua UART1
            HAL_UART_Transmit(&huart1, (uint8_t*)msg, len, 100);

            // 4. Đảo trạng thái LED PC13 để báo hiệu nhận thành công
            HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
        }
    }
}
